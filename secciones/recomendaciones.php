<section class="container my-5">
    <h2 class="text-center mb-4">Recomendaciones</h2>
    <p class="lead text-justify">
        La <strong>calidad del software</strong> es un compromiso continuo que requiere disciplina, planificación y
        buenas prácticas en todas las etapas del ciclo de vida del desarrollo. A continuación, se presentan
        recomendaciones clave para mantener la calidad en proyectos de software, especialmente en entornos educativos.
    </p>

    <div class="mt-4">
        <h4>Recomendaciones Generales</h4>
        <ul>
            <li>
                Implementar <strong>estándares internacionales</strong> como ISO/IEC 25000 e ISO/IEC 12207 para guiar
                los procesos de desarrollo y evaluación del software.
            </li>
            <li>
                Fomentar una <strong>cultura de pruebas continuas</strong>, desde las pruebas unitarias hasta las pruebas
                de aceptación por parte del usuario.
            </li>
            <li>
                Documentar de forma clara los requerimientos, diseños y cambios en el código fuente.
            </li>
            <li>
                Usar <strong>repositorios Git</strong> para controlar versiones y mejorar la colaboración entre equipos.
            </li>
            <li>
                Priorizar la <strong>usabilidad y accesibilidad</strong> en las aplicaciones, asegurando que todos los usuarios
                puedan interactuar de forma intuitiva.
            </li>
            <li>
                Monitorear el rendimiento del software en producción y aplicar mejoras de manera iterativa.
            </li>
        </ul>
    </div>

    <div class="mt-4">
        <h4>Recomendaciones para Entornos Educativos</h4>
        <ul>
            <li>
                Desarrollar herramientas educativas que promuevan el aprendizaje activo y la retroalimentación inmediata.
            </li>
            <li>
                Mantener los sistemas actualizados y protegidos frente a vulnerabilidades de seguridad.
            </li>
            <li>
                Evaluar periódicamente la calidad del software con métricas cuantitativas para garantizar su mejora continua.
            </li>
            <li>
                Capacitar a los equipos docentes y técnicos en buenas prácticas de desarrollo y gestión de calidad.
            </li>
        </ul>
    </div>

    <div class="alert alert-primary mt-4">
        <h5>Conclusión de las Recomendaciones</h5>
        <p>
            Aplicar estas recomendaciones contribuye a obtener software más confiable, eficiente y orientado a las necesidades
            reales de los usuarios. La calidad debe verse como una inversión estratégica que garantiza la sostenibilidad
            tecnológica y el éxito de los proyectos educativos.
        </p>
    </div>

